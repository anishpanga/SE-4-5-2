    public ProjectMaintenance(JFrame  panelHolder, SystemData systemData) {
        this.panelHolder = panelHolder;
        this.systemData = systemData;  
        initComponents();
        ConnectionManager cm = new ConnectionManager();
        EntityManager em = cm.getEntityManager();
        Query query = em.createQuery("Select p  from Project p");
        projectList = query.getResultList();
        
        Object[] columnNames = {"Project (Project Number)", "Client", "State Date",
            "End Date", "Status", "Project Manager", "Client Contact", "Budget"};
        Object[][] rowData = new Object[projectList.size()][9];
        int i =0;
        for(Project project: projectList){
            rowData[i][0] = (Object) project.getName()+" ("+ project.getId() +")";
            rowData[i][1] = (Object) project.getClient();
            rowData[i][2] = (Object) project.getStartDate();
            rowData[i][3] = (Object) project.getEndDate();
            rowData[i][4] = (Object) project.getStatus();
            rowData[i][5] = (Object) project.getProjectManager();
            rowData[i][6] = (Object) project.getClientContact();
            rowData[i][7] = (Object) project.getBudget();
            ++i;
        };
        
        DefaultTableModel tableModel = new DefaultTableModel(rowData, columnNames);
        jTable1.setModel(tableModel);
        
        cm.close();
        
        
    }


